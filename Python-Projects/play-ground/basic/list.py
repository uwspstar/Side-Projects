numbers = [5, 6, 7, 8, 9, 10]
# index
numbers.index(6)  # 1
numbers.index(9)  # 4

# reverse
first_list = [1, 2, 3, 4]
first_list.reverse()
print(first_list) # [4, 3, 2, 1]

# sort
another_list = [6, 4, 1, 2, 5]
another_list.sort()
print(another_list) # [1, 2, 4, 5, 6]

# join
words = ['Coding', 'Is', 'Fun!']
' '.join(words) # 'Coding is Fun!'